# Importing the required module
import matplotlib.pyplot as plt

# X-axis values
x = [1, 2, 3]

# Corresponding Y-axis values
y = [2, 4, 1]

# Plotting the data
plt.plot(x, y)

# Naming the axes
plt.xlabel('x - axis')
plt.ylabel('y - axis')

# Giving a title to the graph
plt.title('My first graph!')

# Display the plot
plt.show()
